package com.football.matches.livescores.pojo;

public class Team {
    private String name;
    private String logo;
    private int id;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLogo() {
        return logo;
    }

    public int getId() {
        return id;
    }

}
